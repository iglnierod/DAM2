    private void updateDisplay(String txt) {
        String texto = displayTextField.getText();
        if (texto.equals("0")) {
            clearDisplay();
            displayTextField.setText(txt);
        } else {
            displayTextField.setText(texto + txt);
        }
    }

    private void clearDisplay() {
        displayTextField.setText("");
    }

    private void resetNumbers() {
        primerNumero = null;
        segundoNumero = null;
    }

    private void guardarNumero(String op) {
        if (primerNumero == null) {
            primerNumero = Integer.parseInt(displayTextField.getText());
        } else if (!ultimoOperador.equals(".")) {
            segundoNumero = Integer.parseInt(displayTextField.getText());
        }
        System.out.println("primerNumero: " + primerNumero);
        System.out.println("segundoNumero: " + segundoNumero);
        clearDisplay();
        if (!op.equals(".")) {
            operador = op;
        }
        ultimoOperador = op;
    }

