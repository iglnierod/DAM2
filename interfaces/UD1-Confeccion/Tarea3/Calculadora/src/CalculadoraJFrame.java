    private void igualButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_igualButtonActionPerformed
        guardarNumero(".");
        switch (operador) {
            case "+":
                updateDisplay(String.valueOf(primerNumero + segundoNumero));
                primerNumero = (primerNumero + segundoNumero);
                break;
            case "-":
                updateDisplay(String.valueOf(primerNumero - segundoNumero));
                primerNumero = primerNumero - segundoNumero;
                break;
            case "*":
                updateDisplay(String.valueOf(primerNumero * segundoNumero));
                primerNumero = primerNumero * segundoNumero;
                break;
            case "/":
                if (primerNumero % segundoNumero == 0) {
                    updateDisplay(String.valueOf(primerNumero / segundoNumero));
                    primerNumero = (primerNumero/segundoNumero);
                } else {
                    updateDisplay(String.format("%.2f", (primerNumero + 0.0) / segundoNumero));
                }
        }
    }//GEN-LAST:event_igualButtonActionPerformed

